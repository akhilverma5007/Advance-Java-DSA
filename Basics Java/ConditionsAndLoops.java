public class ConditionsAndLoops {
    public static void main(String[] args) {
        if(true){
            System.out.println("Its True");
        }

        //while loop
        int count = 1;
        while(count <= 10){
            System.out.println(count);
            count ++;
        }

        //for loop
        for(int i = 1; i<=10; i++){
            System.out.println(i);
        }
    }
}
 